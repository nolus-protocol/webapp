        Protocol and the respective DEX and Gas as per Section C.4. All Swap Fees and Gas shall be deductible from
        User’s balance upon the Exchange operation.
      </p>
      <p>6. <strong>Transactional Gas</strong></p>
      <p>
        6.1. The User understands and acknowledges that each transaction on the Protocol is related to consumption of
        Transactional Gas, which the Protocol shall automatically tap from User’s funds.
      </p>
      <p>
        6.2. The User understands and acknowledges that Transactional Gas shall always be consumed in NLS native tokens,
        as if the User’s funds are in other denomination of Digital Asset, the Protocol shall automatically procure
        exchange for buying NLS needed to cover the amount of Transactional Gas.
      </p>
      <p>
        6.3. The methodology of estimation of the amount of Transactional Gas is initially configured in the Protocol
        and can be changed only based on explicit DAO decision.
      </p>
      <p>7. <strong>Staking</strong></p>
      <p>7.1. The Protocol is created on a Proof – of – Stake principle and operating through its generic coin NLS.</p>
      <p>
        7.2. Each User which holds NLS tokens shall be able to stake NLS to support the Protocol operations and
        expansion.
      </p>
      <p>7.3. Staking terms and rewards shall be set in the Nolus DAO Governance Terms, available on the Website.</p>
      <h1>SECTION D</h1>
      <h2>Lease</h2>
      <br />
      <p>
        <strong> I. DeFi Lease </strong>
      </p>
      <p>
        1. Subject to eligibility with predetermined Protocol parameters each User shall be entitled to use Leased
        crypto financing from the Protocol for the purchase of Digital Asset/s.
      </p>
      <p>
        2. All Leases shall be provided only in the Digital Asset relevant to the utilized LPP (LPN of Liquidity
        Provider’s Pools existent on the Protocol).
      </p>
      <p>
        <strong> II. Borrow Requests </strong>
      </p>
      <p>
        1. А User willing to receive Lease financing shall submit a Borrow Request to the Lease Factory in a standard
        form.
      </p>
      <p>2. The standard Borrow Request form requires the User to fill in the requested information as follows:</p>
      <p>2.1. Requested denomination of the Digital Asset to be borrowed;</p>
      <p>2.2. The Digital Asset, which shall be purchased with the Lease;</p>
      <p>2.3. Amount and denomination of the Digital Asset, which will serve as Down payment;</p>
      <p>
        3. If the Borrow Request is compliant with these Statues and the approved Protocol algorithms, the Protocol
        shall provide quote to the User, containing the main parameters of the Lease. Quotes shall also indicate expiry
        period, after which such quotes shall no longer be valid.
      </p>
      <p>4. Protocol shall not allow submission of any non – compliant Borrow Requests.</p>
      <p>
        <strong> III. Interest rate calculation </strong>
      </p>
      <p>
        1. The Interest rate will be formed for each respective Lease by Interest Algorithm integrated into the
        Protocol, which shall take into account various parameters, including but not limited to: level of Utilization
        credit history of the Borrower, amount of the Lease requested.
      </p>
      <p>
        2. Once formed under the rules of Section D.III.1. above and fixed in the Lease contract, the Interest rate is
        fixed for the entire duration of the Lease.
      </p>
      <p>
        3. Each User hereby fully agrees and accepts the Interest Algorithm as an independent programming tool
        determinant to the Interest rate both for Lease Interest and for Margin Interest at each respective moment.
      </p>
      <p>
        4. The Protocol shall provide lower Interest rates for Stakers willing to receive Lease financing based on their
        staking amount and duration.
      </p>
      <p>
        <strong> IV. Lease Contract </strong>
      </p>
      <p>
        1. In order to activate the Lease Contract, the Borrower shall have to confirm the respective quote and
        contribute the Down payment in accordance with the rules set in Section D.V below.
      </p>
      <p>2. The Lease Contract shall contain the following data:</p>
      <p>2.1. Borrower’s Wallet address;</p>
      <p>2.2. The LPN of the lent funds;</p>
      <p>2.3. Fixed Interest rate, applicable for the respective Lease;</p>
      <p>2.4. Initial LTV;</p>
      <p>2.5. Max LTV;</p>
      <p>2.6. Healthy LTV;</p>
      <p>2.7. Interest Period</p>
      <p>2.8. Due Date;</p>
      <p>2.9. Grace period;</p>
      <p>2.10. LPP address;</p>
      <p>
        3. To the extent the Interest is duly and timely paid and no Liquidation event occurs, the Lease shall be
        considered executed for an indefinite period and the date/s of full or partial Repayment of the Principal shall
        be at the discretion of the Borrower.
      </p>
      <p>
        4. Lease shall be effective until the occurrence of the earlier event between: (i) full Repayment of the
        Principal plus all outstanding Interest and all transaction fees, or (ii) Total Liquidation event. The effective
        period of the Lease shall also be referred to as the Tenure of the Lease.
      </p>
      <p>
        <strong> V. Down payment </strong>
      </p>
      <p>
        1. The amount of the Down payment of the User shall determine the maximum level of Lease such User will be
        allowed to open in the Protocol. The maximum level of the Lease corresponding to the Down payment level selected
        by the User, shall be displayed in the quote, generated by the Protocol.
      </p>
      <p>2. The User can make the Down payment in one of the following options:</p>
      <p>2.1. in the LPN currency;</p>
      <p>2.2. in another supported Digital Asset currency, different than the leased Digital Asset;</p>
      <p>
        2.3. in the same type of Digital Asset as the leased, in which case the Down payment shall directly be added to
        the Principal to form the Collateral.
      </p>
      <p>
        3. For avoidance of any doubt, irrespectively of the denomination of the Down payment, the entire liability of
        the Borrower shall be calculated, charged and due in the respective LPN currency.
      </p>
      <p>
        <strong> VI. Purchase of the Digital Asset </strong>
      </p>
      <p>
        1. In the cases under Section D.V.2.1 and Section D.V.2.2., when the Digitial Asset leased is different than the
        denomination of the Down payment, the Protocol shall use the Down payment and the Principal to purchase the
        respective amount of the leased Digital Asset/s.
      </p>
      <p>
        2. In the case under Section D.V.2.3 the Protocol shall use only the Principal to purchase the respective amount
        of Digital Assets corresponding to the amount of the Principal.
      </p>
      <p>3. The Protocol will purchase the Digital Asset from a pool on a supported decentralized exchange.</p>
      <p>
        <strong> VII. Collateral </strong>
      </p>
      <p>
        1. In the case under Section D.VI.1, the Digital Asset purchased shall be locked as Collateral in an isolated
        Lease Smart Contract instance on the Protocol, connected to the Wallet of the Borrower, against eventual non –
        performance of Borrower’s liabilities under the Lease Contract.
      </p>
      <p>
        2. In the case under Section D.VI.2. the Down payment and the Digital Asset, purchased with the Principal, shall
        together be locked as a Collateral in an isolated Lease Smart Contract instance on the Protocol, connected to
        the Waller of the Borrower.
      </p>
      <p>
        3. All locked Digital Assets under this Section VII shall be unlocked only upon full Repayment of the respective
        balance of the Lease (together with all accrued Interest due).
      </p>
      <p>
        4. Each User hereby explicitly agrees that the collateralized Digital Asset shall be used as a property, which
        the Protocol, in case of а Liquidation event, can automatically sell in full or partially in order to pay off
        part or the entire Lease liability.
      </p>
      <p>
        <strong> VIII. Payments & Repayment </strong>
      </p>
      <p>1. The Borrower is obliged to pay each due Interest payment not later than on the Due Date.</p>
      <p>
        2. Should the Borrower fails to pay Interest for а certain Interest Period on the Due date, after expiry of the
        Grace period, Liquidation event will be triggered.
      </p>
      <p>3. Borrower can repay fully or partially the Lease at any moment of its Tenure.</p>
      <p>
        4. Any partial repayment shall be used firstly to cover any outstanding Interest payment (first Margin Interest
        and then Lease Interest) and the remaining balance shall reduce the outstanding Principal.
      </p>
      <p>
        5. For full Repayment the Borrower should pay an amount equal to the all outstanding Interest and the
        outstanding Principal together with all related fees and costs including transactional costs.
      </p>
      <p>
        6. Upon full Repayment the Protocol will automatically close the Lease and Collateral positions and will unlock
        the full amount of the Digital Asset/s to the Wallet of the Borrower.
      </p>
      <p>7. Each Payment or Repayment is allowed only in the LPN of the Lease.</p>
      <p>
        <strong> IX. Liability Levels & Re-evaluation </strong>
      </p>
      <p>
        1. All Users hereby acknowledge that the sustainability of the Platform can only be kept as far as an adequate
        balance is always maintained between the Current Price of the Asset (which functions also as Collateral) and the
        current аmount of the Lease (LTV).
      </p>
      <p>
        2. Therefore, the Protocol shall maintain LTV level indicators, which ensure that the аmount of the Lease is
        always kept beneath Max LTV, or otherwise Liquidation under Section D.X is started by the Protocol.
      </p>
      <p>
        3. In order to assess the LTV, the Protocol shall regularly procure Re-evaluation., which shall check and
        reassess the Current Asset Value. Re-evaluation shall be based on data gathered from the Market Data Feed.
      </p>
      <p>4. Based on the Re-evaluation, the Protocol shall monitor the LTV regarding certain Lease.</p>
      <p>
        5. When the Asset depreciates, the Protocol shall monitor if the LTV is close to reaching either Healthy LTV
        Liability level or Max LTV.
      </p>
      <p>
        6. In the case under Section D.IX.5. the Protocol shall send to the Borrower Liquidation warning, which will
        contain information on:
      </p>
      <p>6.1. the Current Asset Value;</p>
      <p>6.2. the current LTV regarding the Lease;</p>
      <p>
        6.3. information on the amount of the Margin Payment the Borrower should make in order to reach Healthy LTV for
        the Lease;
      </p>
      <p>
        7. Based on the Liquidation warning, the Borrower shall be able to make Margin Payment in LPN by transfer to
        corresponding Lease contract, in which the Collateral is locked.
      </p>
      <p>
        8. Should the Borrower fails to make the Margin Payment needed, the Protocol shall trigger the Liquidation
        process under Section D.X.
      </p>
      <p>
        9. For avoidance of any doubt, LTV levels shall be determined by the Protocol for each Lease Contract, based on
        mathematical configurations existing in the Protocol.
      </p>
      <p>
        <strong> X. Liquidation process </strong>
      </p>
      <p>
        1. Based on the procedure set in Section D.IX. the Protocol shall be entitled to trigger Liquidation in case the
        LTV reaches Max LTV and provided that the Borrower hasn’t made a sufficient Margin Payment for reaching Healthy
        LTV.
      </p>
      <p>
        2. In case of LTV reaches Max LTV, the Protocol shall, depending on the case, automatically procure Partial or
        Total Liquidation with respect to the Collateral.
      </p>
      <p>
        2.1. In case of Partial Liquidation the Protocol shall sell a portion of the Collateral, which will generate
        sales revenue to repay the current Lease liability until LTV reaches a Healthy LTV level.
      </p>
      <p>2.2. Partial Liquidation shall be triggered each time the LTV reaches the Max LTV.</p>
      <p>
        3. Total Liquidation shall be the final Liquidation process, in which the Protocol shall sell the balance of the
        Collateral and shall use the proceeds thereof to repay the outstanding Lease liability.
      </p>
      <p>
        4. Upon the occurrence of a Total Liquidation event under Section D.X.3., the Lease will be considered
        terminated at the moment the Total Liquidation is performed. The Borrower shall have no claim whatsoever, except
        for any proceeds of the sale of the collateralized Digital Asset, left after repayment of the Lease (if any).
      </p>
